		<h1>حول الوزارة</h1>

<p style="font-size:18px;"> هي وزارة مصرية مسؤولة عن توفير وإدارة وتطوير الخدمات الصحية المركزية بشقيها العلاجي والدوائي في جمهورية مصر العربية . يقدر عدد العاملين في مستشفيات الوزارة عام 2014 نحو 70 ألف طبيب بشري، ويقدر عدد الصيادلة ب20 ألفا، ويبلغ عدد الأطباء البيطرين، نحو 15 ألف طبيب.
    <br>
    أنشئت في يناير 1936 في أواخر أيام الملك أحمد فؤاد الأول الذي كان لديه غيبوبة سكر وعالجه طبيبه الخاص المصري وعندما أفاق من هذه الغيبوبة قال للطبيب أشكرك يا وزير.

وبناء على هذه العبارة صدر مرسوم ملكي بإنشاء وزارة الصحة العمومية في يناير 1936 نشأت على أساس مجموعة من المصالح العمومية مثل:


مصلحة المستشفيات العامة
مصلحة الصحة القروية
مصلحة الحميات.

